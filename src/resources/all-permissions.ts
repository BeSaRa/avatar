function extractGroup(prefix: string): (keyof typeof ALL_PERMISSIONS)[] {
  return permissionKeys.filter(key => key.startsWith(prefix))
}

function extractGroupIncludes(sub: string): (keyof typeof ALL_PERMISSIONS)[] {
  return permissionKeys.filter(key => key.includes(sub))
}

